#include "libft.h"

int		ft_strequ(const char *s1, const char *s2)
{
	int cmp;

	if (!s1 || !s2)
		return (0);
	cmp = ft_strcmp(s1, s2);
	if (cmp == 0)
		return (1);
	return (0);
}
